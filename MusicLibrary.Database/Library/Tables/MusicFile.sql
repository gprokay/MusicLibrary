CREATE TABLE [Library].[MusicFile]
(
	[Id] INT NOT NULL PRIMARY KEY IDENTITY(1,1)
	,[TrackId] INT NULL FOREIGN KEY REFERENCES [Library].[Track](Id)
	,[FileId] NVARCHAR(4000) NOT NULL UNIQUE
	,[MachineId] NVARCHAR(100) NOT NULL
	,[DriveType] NVARCHAR(50) NOT NULL
	,[Name] NVARCHAR(2000) NOT NULL
	,[Root] NVARCHAR(5) NOT NULL
	,[Length] INT NOT NULL
)
